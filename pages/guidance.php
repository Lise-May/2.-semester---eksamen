                <div class="line guidance_line"></div>
                <p class="text_padding3">Draupnir-sadlerne kan vælges med latex- eller uldstopning.</p>
                <h3 class="text_padding">Uld</h3>
                <p class="text_padding3">Uldstoppede sadelpuder kan tilpasses den enkelte hests ryg ned til mindste detalje af en kompetent sadelmager. Dette giver den ultimativt bedste tilpasning af sadlen. Sadelmageren anbefaler at en uldstoppet sadel tjekkes hvert halve år for, om den stadig passer til hesten og dens muskelforandringer i ryggen. Ulden, som Draupnir-sadlerne kommer med, er lagt i lange baner, så det ikke klumper sammen ved kontakt. Ulden er desuden af rigtig god kvalitet med god spændstighed og affjedring.</p>
                <h3 class="text_padding">Latex</h3>
                <p class="text_padding3">Latexfyldte sadelpuder kan ikke justeres af en sadelmager. De kommer istedet med en standardform, der er udviklet med stor perfektion ud fra studier udført i samarbejde med kiroprakter, dyrlæge og anerkendte ryttere. Da sadelpudernes form ikke kan ændes, er det vigtigt, at sadlens bomtype passer hesten fra start. Herefter er det muligt, at tilpasse sadlen ved hjælp af korrektionsunderlag. Fordelen ved latexfyldte sadelpuder er, at de ikke kræver omstopning eller lignende vedligehold som uldstopning gør. Desuden kan latexsadler benyttes på flere forskellige heste med korrektionsunderlag, og er ikke blot tilpasset én enkelt hest.</p>
