                        <br />Draupnir i Norge producerer rideudstyr i høj kvalitet. Særligt sadeludvikling med henblik på tæt konktakt mellem hest og rytter, har siden Draupnirs begyndelse været fokusområde. Draupnir ønsker en konstant udvikling af deres produkter ud fra nyeste viden om moderne teknologi samt feedback fra topryttere. Med moderne teknologi skal rideoplevelsen forbedres for både hest såvel som rytter.
