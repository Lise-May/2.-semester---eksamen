                        <br />Draupnir blev grundlagt af Tonje Lund Ness og John Ragnvald Ness som et familieprojekt. Tonje er veterinærsygeplejerske med speciale i kiropraktik, kranio-sakral terapi, massage m.v. til heste. Hun har arbejdet som specialist i alternativ behandling af heste siden 2008. Hun behandler rundt regnet 200 heste hver måned og er også en af de førende veterinærsygeplejersker inden for dette felt i Norge. Tonjes interesse for heste startede da hun fik sin første hest i en alder af 11 år. Siden har hun elsket at arbejde med dem både på hobby-plan men også professionelt. 
